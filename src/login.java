    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //login button
                int check = 0;
        String email = jTextField1.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        // Check if fields are empty
        if (email.equals("") || password.equals("")) {
            check = 1;
            JOptionPane.showMessageDialog(null, "Every Field Is Required");
        } 
        // Hardcoded admin credentials check
        else if (email.equals("hms") && password.equals("admin")) {
            check = 1;
            setVisible(false); // Hide current login window
            new adminHome().setVisible(true); // Show admin home page
        } 
        else {
            // Secure query with PreparedStatement to prevent SQL injection
            try (Connection con = ConnectionProvider.getCon();
                 PreparedStatement ps = con.prepareStatement("SELECT * FROM users WHERE email = ? AND password = ?")) {

                ps.setString(1, email);
                ps.setString(2, password);

                // Execute query and process results
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        check = 1;
                        setVisible(false); // Hide current login window
                        new home().setVisible(true); // Show user home page
                    }
                }
            } 
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage());
            }
        }

        // If check is still 0, it means email/password were incorrect
        if (check == 0) {
            JOptionPane.showMessageDialog(null, "Incorrect Email or Password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

